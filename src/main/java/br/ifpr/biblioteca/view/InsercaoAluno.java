    private void popularTabelaView(List<Aluno> listaAluno) {
        
        DefaultTableModel modeloTabela = new DefaultTableModel();
        
        this.TabelaArmario.setModel(modeloTabela);
      
        modeloTabela.addColumn("ID");
        modeloTabela.addColumn("Nome");
        modeloTabela.addColumn("Email");
        modeloTabela.addColumn("Telefone");
        modeloTabela.addColumn("CPF");
        modeloTabela.addColumn("RA");
        modeloTabela.addColumn("Ativo");
        
        for(Aluno aluno: listaAluno) {
            modeloTabela.addRow(
                    new Object[]{
                        aluno.getPessoaID(),
                        aluno.getNome(),
                        aluno.getEmail(),
                        aluno.getTelefone(),
                        aluno.getCPF(),
                        aluno.getRA(),
                        aluno.getAtivo()
                }
            );
        }
      
    }
    
    private void atualizarTabelaView() {
        AlunoDaoImpl dao = new AlunoDaoImpl();
        
        List<Aluno> listaAluno = new ArrayList();
        
        listaAluno = dao.buscarTodos();
        
        this.popularTabelaView(listaAluno);
    }
    
    private void trazerCamposPessoa() {
        
        AlunoDaoImpl daoAluno = new AlunoDaoImpl();  
        
        int idPessoa = Integer.parseInt(lCodigo.getText());
        
        PessoaDaoImpl daoPessoa = new PessoaDaoImpl();
        
        Pessoa pessoa = new Pessoa();
        
        lNome.setText(daoPessoa.buscarPorId(idPessoa).getNome());
        lCPF.setText(daoPessoa.buscarPorId(idPessoa).getCPF());
        lEmail.setText(daoPessoa.buscarPorId(idPessoa).getEmail());
        lTelefone.setText(daoPessoa.buscarPorId(idPessoa).getTelefone());
        lRA.setText(daoAluno.buscarPorId(idPessoa).getRA());

    }

